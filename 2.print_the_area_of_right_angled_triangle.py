# Q2. Write a program that reads the length of the base and the height of a right-angled triangle and prints the area.
# Every number is given on a separate line.

print("This program takes the length of the base and the height of a right-angled triangle and prints the area")

baseLength = float(input("Enter the length of the base of right angled triangle"))
heightLength = float(input("Enter the length of the base of right angled triangle"))

areaOfRightAngledTriangle = 1/2*(baseLength*heightLength)

print("The area of the right angled triangle is",areaOfRightAngledTriangle,"whose base is ",baseLength," and height is ",heightLength,".")



